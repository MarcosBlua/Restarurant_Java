    //Vacia el JTable
    public void vaciarTabla(){
        DefaultTableModel dtm = (DefaultTableModel) jtVentasPorDia.getModel();
        dtm.setRowCount(0);
        jtVentasPorDia.setModel(dtm);
        
        datosTabla.clear();
    }
    
    //Limpia toda la pantalla
    public void clear_T(){
        vaciarTabla();
        jdcVentasPorDia.setCalendar(Calendar.getInstance());
    }
    
    //Carga JTable con datos
    public void cargarJTB(){
        vaciarTabla();
        
        Reporte.ventasPorDia(jdcVentasPorDia.getCalendar(), datosTabla);
        
        ManejoTablas.cargarTabla(datosTabla, modeloTabla);
        
        jtVentasPorDia.setModel(modeloTabla);
    }
    
    //Actualiza la pantalla
    public void actualizarPantalla(){
        cargarJTB();
        
        jdcVentasPorDia.setDate(new java.sql.Date(fechaAct.getTimeInMillis()));
    }
