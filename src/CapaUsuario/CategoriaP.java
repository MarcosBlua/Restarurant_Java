    //Limpia campos de ingreso de datos, desactiva botones y resetea banderas
    public void clear(){
        bandModificar = false;
        btnGuardar.setEnabled(false);
        btnCancelar.setEnabled(false);
        txtNombre.setEnabled(false);
        chxActiva.setEnabled(false);
        
        txtNombre.setText("");
        chxActiva.setSelected(true);
    }
    
    //Limpia toda la pantalla
    public void clear_T(){
        clear();
        
        vaciarTabla();
    }
    
    //Vacia el JTable
    public void vaciarTabla(){
        DefaultTableModel dtm = (DefaultTableModel) jtCategorias.getModel();
        dtm.setRowCount(0);
        jtCategorias.setModel(dtm);
        
        datosTabla.clear();
    }
    
    //Carga JTable con datos
    public void cargarJTB(){
        vaciarTabla();
        
        Categorias.cargarTablaCategoria(datosTabla);
        
        ManejoTablas.cargarTabla(datosTabla, modeloTabla);
        
        jtCategorias.setModel(modeloTabla);
    }
    
    //Activa los campos de carga de datos y botones correspondientes
    public void activarCarga(){
        btnGuardar.setEnabled(true);
        btnCancelar.setEnabled(true);
        txtNombre.setEnabled(true);
        chxActiva.setEnabled(true);
    }
    
    //Actualiza la pantalla
    public void actualizarPantalla(){
        cargarJTB();
    }
