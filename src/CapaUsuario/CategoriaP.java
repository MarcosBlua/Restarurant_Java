    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        if(!bandModificar){
            cat = new Categoria(txtNombre.getText(), chxActiva.isSelected());
            
            if(cat.getError()){
                Mensajes.msjError(cat.getMensaje());
            }else{
                cat.guardarCategoria();
                
                clear();
        
                cargarJTB();
            }
        }else{
            cat.modificarCategoria(txtNombre.getText(), chxActiva.isSelected());
            
            if(cat.getError()){
                Mensajes.msjError(cat.getMensaje());
            }else{
                clear();
        
                cargarJTB();
            }
        }
    }//GEN-LAST:event_btnGuardarMouseClicked

    private void btnModificarCatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnModificarCatMouseClicked
        bandModificar = true;
        
        //Utiliza el numero de row para verificar que haya un elemento seleccionado
        int row = jtCategorias.getSelectedRow();
        if(row ==-1){
            Mensajes.msjError("No hay categorias cargadas o no selecciono una.");
        }else{
            cat = Categoria.traerUnaCategoria((Integer)jtCategorias.getValueAt(row, 0));
            
            activarCarga();
            
            txtNombre.setText(cat.getNombre());
            chxActiva.setSelected(cat.isBaja());
        }
        
    }//GEN-LAST:event_btnModificarCatMouseClicked

